#include <init.h>
#include <onewire.h>

static uint8_t lock = 0;

int ow_lock(void) {
	if (lock == 0) {
		lock = 1;
		return 1;
	}
	
	return 0;
}

int ow_unlock(void) {
	lock = 0;
	return 0;
}

